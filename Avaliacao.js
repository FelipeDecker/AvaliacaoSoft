var incrementarData = function (data) {
    let dia = data.getDate()
    data.setDate(dia + 1)
}

var transformarEmData = function (data) {
    let stringQuebrada = data.split("/")
    let dia = stringQuebrada[0]
    let mes = stringQuebrada[1]
    let ano = stringQuebrada[2]
    let novaData = new Date(ano, mes, dia)
    return novaData
}

var formatarData = function (data) {
    let dia = data.getDate()
    if (dia < 10) {
        dia = `0${dia}`
    }
    let mes = data.getMonth()
    if (mes < 10) {
        mes = `0${mes}`
    }
    let ano = data.getFullYear()
    let novaData = `${dia}/${mes}/${ano}`
    return novaData
}

var pegarPreco = function (data) {
    var result = ultimoValorCombustivel

    for (const obj in preco) {
        let dataElemento = transformarEmData(preco[obj].date)
        let diaElemento = dataElemento.getDate()
        let mesElemento = dataElemento.getMonth()
        let anoElemento = dataElemento.getFullYear()

        let diaParametro = data.getDate()
        let mesParametro = data.getMonth()
        let anoParametro = data.getFullYear()

        let valor = preco[obj].value

        if (diaParametro == diaElemento && mesParametro == mesElemento && anoParametro == anoElemento) {
            result = valor
            break
        } else {
            result = ultimoValorCombustivel
        }
    }

    ultimoValorCombustivel = result

    return result
}

